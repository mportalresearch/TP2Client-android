package com.mportal.sipdata;

public class SipFriend
{
    private String address;

    private String status;

    private boolean isCustomePresence;
    
    private String customMessage;
    
    public String getCustomMessage()
    {
	return customMessage;
    }
    
    public void setCustomMessage(String customMessage)
    {
	this.customMessage = customMessage;
    }

    public boolean isCustomePresence()
    {
	return isCustomePresence;
    }

    public void setCustomePresence(boolean isCustomePresence)
    {
	this.isCustomePresence = isCustomePresence;
    }

    public void setAddress(String address)
    {
	this.address = address;
    }

    public String getAddress()
    {
	return address;
    }

    public void setStatus(String status)
    {
	this.status = status;

    }

    public String getStatus()
    {
	return status;
    }

}
